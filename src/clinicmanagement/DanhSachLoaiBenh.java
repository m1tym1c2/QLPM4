    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try
        {
            String s = (String) JOptionPane.showInputDialog(this,"Nhập loại bệnh mới: ");
            DatabaseConnection DTBC = new DatabaseConnection();
            Connection conn = DTBC.getConnection(this);
            Statement stm = conn.createStatement();
            DefaultTableModel model = (DefaultTableModel) Table.getModel();
            boolean flag = true;
            String MaLoaiBenh = "LB";
            int stt = 1;
            do {
                flag = true;
                MaLoaiBenh = "LB" + String.valueOf(stt);
                ResultSet rs = stm.executeQuery("SELECT MaLoaiBenh FROM LOAIBENH");
                while (rs.next()) {
                    if (MaLoaiBenh.equals(rs.getString(1))) 
                    {
                        flag = false;
                        break;
                    }
                }
                stt++;
            } while (!flag);
            stm.executeUpdate("INSERT INTO LOAIBENH VALUES ('" + MaLoaiBenh + "',N'"+s+"')");
            HienThi();
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

