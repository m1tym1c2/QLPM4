        DefaultTableModel model = (DefaultTableModel) Table.getModel();
        if (Table.getSelectedRow() == -1)
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một loại bệnh", "Cảnh báo chưa chọn loại bệnh", 2);
        else {
            try
            {
                DatabaseConnection DTBC = new DatabaseConnection();
                Connection conn = DTBC.getConnection(this);
                Statement stm = conn.createStatement();
                stm.executeUpdate("DELETE FROM LOAIBENH WHERE TenLoaiBenh = N'"+model.getValueAt(Table.getSelectedRow(), 1)+"'");
                HienThi();
            }
            catch (Exception e)
            {
                
            }
        }
